package Bank.Model;

public class UserInfoModels {

	private String userId;
	private String fName;
	private String lName;
	private String dob;
	private String fatherName;
	private String addresss;
	private String city;
	private String district;
	private String state;
	private String pincode;
	private String phoneNo;
	private String email;
	private String emergencyNo;
	private String aadgarNo;
	private String panNo;
	private String accountNo;
	



	public UserInfoModels() {
		super();
	}


	public UserInfoModels(String userId, String fName, String lName, String dob, String fatherName, String addresss,
			String city, String district, String state, String pincode, String phoneNo, String email,
			String emergencyNo, String aadgarNo, String panNo, String accountNo) {
		super();
		this.userId = userId;
		this.fName = fName;
		this.lName = lName;
		this.dob = dob;
		this.fatherName = fatherName;
		this.addresss = addresss;
		this.city = city;
		this.district = district;
		this.state = state;
		this.pincode = pincode;
		this.phoneNo = phoneNo;
		this.email = email;
		this.emergencyNo = emergencyNo;
		this.aadgarNo = aadgarNo;
		this.panNo = panNo;
		this.accountNo = accountNo;
	}


	public String getUserId() {
		return userId;
	}


	public void setUserId(String userId) {
		this.userId = userId;
	}


	public String getfName() {
		return fName;
	}


	public void setfName(String fName) {
		this.fName = fName;
	}


	public String getlName() {
		return lName;
	}


	public void setlName(String lName) {
		this.lName = lName;
	}


	public String getDob() {
		return dob;
	}


	public void setDob(String dob) {
		this.dob = dob;
	}


	public String getFatherName() {
		return fatherName;
	}


	public void setFatherName(String fatherName) {
		this.fatherName = fatherName;
	}


	public String getAddresss() {
		return addresss;
	}


	public void setAddresss(String addresss) {
		this.addresss = addresss;
	}


	public String getCity() {
		return city;
	}


	public void setCity(String city) {
		this.city = city;
	}


	public String getDistrict() {
		return district;
	}


	public void setDistrict(String district) {
		this.district = district;
	}


	public String getState() {
		return state;
	}


	public void setState(String state) {
		this.state = state;
	}


	public String getPincode() {
		return pincode;
	}


	public void setPincode(String pincode) {
		this.pincode = pincode;
	}


	public String getPhoneNo() {
		return phoneNo;
	}


	public void setPhoneNo(String phoneNo) {
		this.phoneNo = phoneNo;
	}


	public String getEmail() {
		return email;
	}


	public void setEmail(String email) {
		this.email = email;
	}


	public String getEmergencyNo() {
		return emergencyNo;
	}


	public void setEmergencyNo(String emergencyNo) {
		this.emergencyNo = emergencyNo;
	}


	public String getAadgarNo() {
		return aadgarNo;
	}


	public void setAadgarNo(String aadgarNo) {
		this.aadgarNo = aadgarNo;
	}


	public String getPanNo() {
		return panNo;
	}


	public void setPanNo(String panNo) {
		this.panNo = panNo;
	}


	public String getAccountNo() {
		return accountNo;
	}


	public void setAccountNo(String accountNo) {
		this.accountNo = accountNo;
	}
	
	
	
	

}
